/*
    Array In JavaScript:
    1. Array is a collection of elements of the same type stored in contiguous memory locations.
    2. It is a data structure that stores elements of the same data type.
    3. The size of an array is fixed.
    4. The size of an array must be specified before storing elements in it.
    5. The size of an array cannot be changed once it is declared.
*/

/* 
    JavaScript Array Methods:
    1. Mutator Methods (Modify the original array):
        - push(): Adds one or more elements to the end of an array.
        - pop(): Removes the last element from an array.
        - shift(): Removes the first element from an array. 
        - unshift(): Adds one or more elements to the beginning of an array.
        - splice(): Adds or removes elements from an array.
        - sort(): Sorts the elements of an array.                               
        - reverse(): Reverses the order of the elements of an array.
        - fill(): Fills all the elements of an array from a start index to an end index with a static value.
        - copyWithin(): Copies array elements within the array, to and from specified positions.

    2. Accessor Methods (Do not modify the original array):
        - concat(): Merges two or more arrays.
        - includes(): Determines whether an array includes a certain element.
        - indexOf(): Returns the first index at which a given element can be found in the array.
        - lastIndexOf(): Returns the last index at which a given element can be found in the array.
        - join(): Joins all elements of an array into a string.
        - slice(): Extracts a section of an array and returns a new array.
        - toString(): Returns a string representing the array.
        - toLocaleString(): Returns a localized string representing the array.
        - isArray(): Determines whether the passed value is an array.
    
    3. Iteration Methods:
        - forEach(): Calls a function for each element in the array.
        - map(): Creates a new array with the results of calling a provided function on every element in the array.
        - filter(): Creates a new array with all elements that pass the test implemented by the provided function.
        - reduce(): Applies a function against an accumulator and each element in the array (from left to right) to reduce it to a single value.
        - reduceRight(): Applies a function against an accumulator and each element in the array (from right to left) to reduce it to a single value.
        - every(): Tests whether all elements in the array pass the test implemented by the provided function.
        - some(): Tests whether at least one element in the array passes the test implemented by the provided function.
        - find(): Returns the value of the first element in the array that satisfies the provided testing function.
        - findIndex(): Returns the index of the first element in the array that satisfies the provided testing function.
    
    4. Length Methods:
        - length: Returns the number of elements in an array.        
*/